    def start_requests(self):
        file = open("./stock_id.txt", encoding="utf-8")
        # file2 = open("./stock_name.txt", encoding="utf-8")
        for index, stock_id in enumerate(file.readlines()):
            url = self.prefix_url.format(stock_id.replace("\n", ""))
            yield Request(url, callback=self.parse, dont_filter=True,headers=self.headers,
                          meta={"astock_id": stock_id.replace("\n", ""), "astock_name": self.astock_names[index]})

    def parse(self, response):
        meta = deepcopy(response.meta)
        day, open, high, low, close, volume = "day", "open", "high", "low", "close", "volume"
        res = response.body.decode()

        for msg in eval(res):
            print("----------",msg)

            # 日期
            date = msg["day"]

            # 成交量
            volume = msg["volume"]

            # 开盘价
            open = msg["open"]

            # 收盘价
            close = msg["close"]

            # 最高价
            high = msg["high"]

            # 最低价
            low = msg["low"]

            astock = AstockItem()
            astock["astock_id"] = meta["astock_id"]
            astock["astock_name"] = meta["astock_name"]
            astock["date"] = date
            astock["volume"] = volume
            astock["open"] = open
            astock["close"] = close
            astock["high"] = high
            astock["low"] = low
            yield astock
